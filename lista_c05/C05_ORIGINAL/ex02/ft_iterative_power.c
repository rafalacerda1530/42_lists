#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int result;
	int cont;

	cont = 1;
	result = 1;
	if (power < 0)
	{
		return (0);
	}
	if (nb == 0 && power == 0)
	{
		return (1);
	}
	while (cont <= power)
	{
		result = result * nb;
		cont++;
	}
	return (result);
}
